// Helpers to pull content from markdown sources
const desc = (slug: string): string => {
  const entry = caseStudyMap[slug];
  if (!entry) return '';
  return extractFirstParagraph(entry.shortMd);
};

const sit = (slug: string): string => {
  const entry = caseStudyMap[slug];
  if (!entry) return '';
  const section =
    extractSection(entry.fullMd, 'Problem & Goals') ||
    extractSection(entry.fullMd, 'Project Overview') ||
    extractSection(entry.fullMd, 'The Problem') ||
    '';
  return extractFirstParagraph(section);
};

const appr = (slug: string): string => {
  const entry = caseStudyMap[slug];
  if (!entry) return '';
  const section =
    extractSection(entry.fullMd, 'Process & Approach') ||
    extractSection(entry.fullMd, 'Approach') ||
    '';
  return extractFirstParagraph(section);
};

